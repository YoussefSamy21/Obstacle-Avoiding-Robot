#ifndef STD_TYPES_H_
#define STD_TYPES_H_


typedef     unsigned char         uint8   ;
typedef     unsigned short int    uint16  ;
typedef     unsigned int          uint32  ;

typedef     signed char           sint8   ;
typedef     signed short int      sint16  ;
typedef     signed int            sint32  ;

typedef     float            	  f32  	  ;
typedef     double            	  f64  	  ;

#define 	NULLPTR  			((void*)0)


#endif /* STD_TYPES_H_ */
